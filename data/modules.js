const learningPaths = examDomains; // backward-compat alias

function findUnit(unitId) {
  for (const d of examDomains) {
    for (const mod of d.modules) {
      for (const unit of mod.units) {
        if (unit.id === unitId) return unit;
      }
    }
  }
  return null;
}

function findModule(moduleId) {
  for (const d of examDomains) {
    for (const mod of d.modules) {
      if (mod.id === moduleId) return mod;
    }
  }
  return null;
}

function getDomainForModule(moduleId) {
  for (const d of examDomains) {
    for (const mod of d.modules) {
      if (mod.id === moduleId) return d;
    }
  }
  return null;
}

// backward compat
function getLearningPathForModule(moduleId) { return getDomainForModule(moduleId); }

