// Generate multiple-choice options from same-category answers
(function() {
  const catPools = {};
  flashcards.forEach(c => {
    if (!catPools[c.category]) catPools[c.category] = [];
    const answers = c.answer.split(/[·;]/).map(a => a.replace(/\*+$/, '').trim()).filter(a => a);
    answers.forEach(a => { if (!catPools[c.category].includes(a)) catPools[c.category].push(a); });
  });
  flashcards.forEach(card => {
    const pool = (catPools[card.category] || []).filter(a => a !== card.answer && !card.answer.includes(a));
    const wrongs = pool.sort(() => 0.5 - Math.random()).slice(0, 3);
    while (wrongs.length < 3) wrongs.push("None of the above");
    const all = [card.answer, ...wrongs].sort(() => 0.5 - Math.random());
    card.options = all;
    card.correctIndex = all.indexOf(card.answer);
  });
})();

